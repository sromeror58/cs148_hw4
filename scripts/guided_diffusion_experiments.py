"""
scripts/guided_diffusion_experiments.py  —  Part 7
====================================================
Helper script for running guided-diffusion experiments on 256×256 images.

NOTE: This script is a thin wrapper — the actual sampling is done via the
OpenAI guided-diffusion CLI.  The functions here handle result loading and
plotting so your Colab notebook stays clean.

Setup (run once in your environment or Colab):
    git clone https://github.com/openai/guided-diffusion
    cd guided-diffusion && pip install -e .
    mkdir -p models
    # Download model weights from the OpenAI model card URL
"""

from __future__ import annotations

import argparse
import os

import matplotlib.pyplot as plt
import numpy as np


def load_npz_samples(npz_path: str) -> np.ndarray:
    """Load samples from a .npz file generated by guided-diffusion.

    Args:
        npz_path: Path to .npz file (e.g. samples_8x256x256x3.npz).

    Returns:
        Array of shape (N, H, W, C), dtype uint8.
    """
    data = np.load(npz_path)
    return data["arr_0"]


def plot_grid(images: np.ndarray, ncols: int = 8, out: str = "grid.png", title: str = ""):
    """Plot images in a row of `ncols` columns and save to disk.

    Args:
        images: (N, H, W, 3) uint8 array.
        ncols:  Number of columns (each image becomes one column).
        out:    Output path.
        title:  Figure title.
    """
    N, H, W, C = images.shape
    fig, axes = plt.subplots(1, ncols, figsize=(ncols * W / 100, H / 100))
    for i, ax in enumerate(axes):
        ax.imshow(images[i] if i < N else np.zeros((H, W, C), dtype=np.uint8))
        ax.axis("off")
    fig.suptitle(title, fontsize=11)
    plt.tight_layout()
    plt.savefig(out, dpi=100, bbox_inches="tight")
    plt.close()
    print(f"Saved: {out}")


# ------------------------------------------------------------------
# Problem 7.1 — Unconditional generation
# ------------------------------------------------------------------

def plot_unconditional(npz_path: str, out: str = "7_1_unconditional.png"):
    """Plot 8 unconditionally generated 256×256 images (Problem 7.1).

    Args:
        npz_path: Path to .npz from guided-diffusion unconditional sampler.
        out:      Output path for the 256×2048 figure.
    """
    images = load_npz_samples(npz_path)  # (N, H, W, 3)
    images = images[:8]                   # take first 8
    plot_grid(images, ncols=8, out=out,
              title="Unconditional 256×256 generation (Problem 7.1)")


# ------------------------------------------------------------------
# Problem 7.2 — Progressive generation
# ------------------------------------------------------------------

def plot_progressive(
    intermediate_npzs: list[str],
    out: str = "7_2_progressive.png",
):
    """Visualise the denoising trajectory (Problem 7.2).

    Args:
        intermediate_npzs: List of 8 .npz paths ordered from t=T to t=0
                           (first = pure noise, last = final image).
        out:               Output path for the 256×2048 figure.
    """
    frames = []
    for path in intermediate_npzs[:8]:
        imgs = load_npz_samples(path)
        frames.append(imgs[0])  # one image per checkpoint

    frames = np.stack(frames, axis=0)   # (8, H, W, 3)
    plot_grid(frames, ncols=8, out=out,
              title="Progressive generation — noise → image (Problem 7.2)")


# ------------------------------------------------------------------
# Problem 7.3 — Noise interpolation
# ------------------------------------------------------------------

def plot_noise_interpolation(npz_path: str, out: str = "7_3_interpolation.png"):
    """Plot 8 samples along a noise interpolation path (Problem 7.3).

    Args:
        npz_path: .npz from guided-diffusion run with interpolated noises.
        out:      Output path for the 256×2048 figure.
    """
    images = load_npz_samples(npz_path)  # (8, H, W, 3)
    images = images[:8]
    plot_grid(images, ncols=8, out=out,
              title=r"Noise interpolation $z_i = (1-i/7)z_0 + (i/7)z_7$ (Problem 7.3)")


# ------------------------------------------------------------------
# Problem 7.4 — Conditional generation
# ------------------------------------------------------------------

def plot_conditional(npz_path: str, class_labels: list[int], out: str = "7_4_conditional.png"):
    """Plot 8 class-conditional generated images (Problem 7.4).

    Args:
        npz_path:     .npz from guided-diffusion classifier-guided sampler.
        class_labels: List of 8 ImageNet class IDs used (for the title).
        out:          Output path.
    """
    images = load_npz_samples(npz_path)
    images = images[:8]
    label_str = ", ".join(str(c) for c in class_labels[:8]) if class_labels else ""
    title = f"Class-conditional generation, classes: [{label_str}] (Problem 7.4)"
    plot_grid(images, ncols=8, out=out, title=title)


# ------------------------------------------------------------------
# Problem 7.5 — Classifier scale sweep
# ------------------------------------------------------------------

def plot_classifier_scale_sweep(
    npz_paths: list[str],
    scale_values: list[float],
    out: str = "7_5_scale_sweep.png",
):
    """Plot 2 rows × 8 columns, one column per classifier scale (Problem 7.5).

    Args:
        npz_paths:    8 .npz files, one per scale value (2 images each).
        scale_values: List of 8 classifier_scale values (monotonically increasing).
        out:          Output path for the 512×2048 figure.
    """
    n_scales = len(npz_paths)
    N, H, W, C = load_npz_samples(npz_paths[0]).shape

    fig, axes = plt.subplots(2, n_scales, figsize=(n_scales * W / 100, 2 * H / 100))

    for col, (path, scale) in enumerate(zip(npz_paths, scale_values)):
        imgs = load_npz_samples(path)
        for row in range(2):
            ax = axes[row, col]
            ax.imshow(imgs[row] if row < len(imgs) else np.zeros((H, W, C), dtype=np.uint8))
            ax.axis("off")
            if row == 0:
                ax.set_title(f"s={scale}", fontsize=8)

    fig.suptitle("Classifier scale sweep (Problem 7.5)", fontsize=11)
    plt.tight_layout()
    plt.savefig(out, dpi=100, bbox_inches="tight")
    plt.close()
    print(f"Saved: {out}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--task", choices=["7_1", "7_2", "7_3", "7_4", "7_5"], required=True)
    parser.add_argument("--npz",  type=str, nargs="+", help="Path(s) to .npz file(s)")
    parser.add_argument("--out",  type=str, default=None)
    args = parser.parse_args()

    if args.task == "7_1":
        plot_unconditional(args.npz[0], out=args.out or "7_1_unconditional.png")
    elif args.task == "7_2":
        plot_progressive(args.npz, out=args.out or "7_2_progressive.png")
    elif args.task == "7_3":
        plot_noise_interpolation(args.npz[0], out=args.out or "7_3_interpolation.png")
    elif args.task == "7_4":
        plot_conditional(args.npz[0], class_labels=[], out=args.out or "7_4_conditional.png")
    elif args.task == "7_5":
        scales = [float(s) for s in args.npz[1:]] if len(args.npz) > 1 else []
        npz_list = [args.npz[0]] * 8 if len(args.npz) == 1 else args.npz[:8]
        plot_classifier_scale_sweep(npz_list, scale_values=scales,
                                    out=args.out or "7_5_scale_sweep.png")
